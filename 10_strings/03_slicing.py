name = "Harry0123456789"

print(name[0:2]) # Goes from 0 to 2-1 i.e 0 to 1

print(name[2:-1]) # Same as num [2,4]

# print(name[0:10:n]) #Skip n-1 charachters

print(name[0:10:1]) # Skip 1-1 i.e 0 charachters
print(name[0:10:3]) # Skip 3-1 i.e 2 charachters

print(name[:4]) # Replace the first empty number with 0
print(name[1:]) # Replace the second empty number with -1